  //Right triangle star
import java.util.Scanner;
class P5{
    public static void main(String args[])
    {
       
        byte i,j;
        for( i=7; i>=1; i--)
        {
          
                for (j=7; j>=i+1; j--){
                System.out.printf("*");                                   
                }
            System.out.printf("\n");
        }
    }

}






















































   



   

